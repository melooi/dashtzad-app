  `<div class="dz-timeline">${items.map((it) => `<div class="dz-timeline__item"><span class="dz-timeline__label">${it.label}</span><div class="dz-timeline__body">${it.text}</div></div>`).join("")}</div>`;
const faq = (items: { q: string; a: string }[]) =>
  `<div class="dz-faq">${items.map((it) => `<div class="dz-faq__item"><p class="dz-faq__q">${it.q}</p><div class="dz-faq__a">${it.a}</div></div>`).join("")}</div>`;
const table = (headers: string[], rows: string[][]) =>
  `<table class="dz-table"><tbody><tr>${headers.map((hh) => `<th>${hh}</th>`).join("")}</tr>${rows.map((r) => `<tr>${r.map((c) => `<td>${c}</td>`).join("")}</tr>`).join("")}</tbody></table>`;
const card = (variant: string, o: { eyebrow?: string; title?: string; text?: string; href?: string; linkLabel?: string }) =>
  `<figure class="dz-card dz-card--${variant}"><div class="dz-card__body">${o.eyebrow ? `<p class="dz-card__eyebrow">${o.eyebrow}</p>` : ""}${o.title ? `<p class="dz-card__title">${o.title}</p>` : ""}${o.text ? `<p class="dz-card__text">${o.text}</p>` : ""}${o.href ? `<a class="dz-card__link" href="${o.href}">${o.linkLabel || o.href}</a>` : ""}</div></figure>`;
const checkList = (items: string[]) => `<ul class="dz-list--check">${items.map((i) => `<li>${i}</li>`).join("")}</ul>`;
const crossList = (items: string[]) => `<ul class="dz-list--cross">${items.map((i) => `<li>${i}</li>`).join("")}</ul>`;
const stepsList = (items: string[]) => `<ol class="dz-list--steps">${items.map((i) => `<li>${i}</li>`).join("")}</ol>`;
const leafList = (items: string[]) => `<ul class="dz-list--leaf">${items.map((i) => `<li>${i}</li>`).join("")}</ul>`;

